import mongoose from "mongoose";

const { Schema, model } = mongoose;

export const UserSchema = new Schema({
    userId : {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true,
    }
});